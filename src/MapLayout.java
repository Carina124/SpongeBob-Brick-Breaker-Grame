    public static void makeWeedMaps(int[] weedMaps) {
        int locationX;
        int locationY;
        int mapNum;

        for (int i = 0; i < weedMaps.length; i++) {
            locationX = 40 * (i % 26);
            locationY = 40 * (i / 26);
            SpriteBricks sb1;
            mapNum = weedMaps[i];
            pastMapIndex = i;
            switch (mapNum) {
                case 0:
                    break;
                case 1:
                    sb1 = new SpriteBricks(locationX, locationY, RainbowReefMain.block1);
                    weedMapsHelper(sb1, mapNum, 50);
                    break;
                case 2:
                    sb1 = new SpriteBricks(locationX, locationY, RainbowReefMain.block2);
                    weedMapsHelper(sb1, mapNum, 100);
                    break;
                case 3:
                    sb1 = new SpriteBricks(locationX, locationY, RainbowReefMain.block3);
                    weedMapsHelper(sb1, mapNum, 150);
                    break;
                case 4:
                    sb1 = new SpriteBricks(locationX, locationY, RainbowReefMain.block4);
                    weedMapsHelper(sb1, mapNum, 200);
                    break;
                case 5:
                    sb1 = new SpriteBricks(locationX, locationY, RainbowReefMain.KrakenBig);
                    sb1.setBrickLifeCount(3);
                    weedMapsHelper(sb1, mapNum, 500);
                    RainbowReefMain.bigLegCount++;
                    break;
                case 6:
                    sb1 = new SpriteBricks(locationX + 20, locationY, RainbowReefMain.KrakenSmall);
                    sb1.setBrickLifeCount(2);
                    weedMapsHelper(sb1, mapNum, 300);
                    RainbowReefMain.bigLegCount++;
                    break;
                case 7:
                    sb1 = new SpriteBricks(locationX, locationY, RainbowReefMain.UnbreakableLong);
                    weedMapsHelper(sb1, mapNum, 0);
                    break;
                case 8:
                    sb1 = new SpriteBricks(locationX, locationY, RainbowReefMain.UnbreakableShort);
                    weedMapsHelper(sb1, mapNum, 0);
                    break;
                case 9:
                    sb1 = new SpriteBricks(locationX, locationY, RainbowReefMain.PowerUpHealth);
                    weedMapsHelper(sb1, mapNum, 0);
                    break;
                case 10:
                    sb1 = new SpriteBricks(locationX, locationY, RainbowReefMain.blockSplit);
                    weedMapsHelper(sb1, mapNum, 0);
                    break;
            }
        }
    }

    private static void weedMapsHelper(SpriteBricks sb1, int i, int brickPointValue) {
        sb1.setBrickType(i);
        sb1.setBlockScore(brickPointValue);

        if (i == 7 || i == 8) {
            sb1.setBreakableTruth(false);
        }
        SpongeBobCollision.blocksList.add(sb1);
        RainbowReefMain.geobv.addObserver(sb1);
    }

    public static void deleteMap() {
        SpriteBricks tempBrick;
        while (!SpongeBobCollision.blocksList.isEmpty()) {
            tempBrick = SpongeBobCollision.blocksList.get(0);
            RainbowReefMain.geobv.deleteObserver(tempBrick);
            SpongeBobCollision.blocksList.remove(tempBrick);
        }
    }
